package com.devqrh.core.checklist;

public class ChecklistStep {

    private int step;
    private String action;

    public int getStep() {
        return step;
    }

    public void setStep(int step) {
        this.step = step;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }
}
